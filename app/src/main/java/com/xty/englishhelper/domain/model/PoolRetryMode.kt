package com.xty.englishhelper.domain.model

/**
 * 词池整理（QUALITY_FIRST）的重试策略。与并发配置一致：修改后从「下一个词」起生效。
 */
enum class PoolRetryMode {
    /**
     * 积极重试（默认，原行为）：
     * - 可重试错误：短延迟（约 1~3 秒 / 退避）后重试，最多 MAX_EDGE_RETRIES 次。
     * - 不可重试错误（鉴权失败、明确的 4xx、解析判定为不可重试等）：立即判定该块失败 → 中止整本构建。
     */
    AGGRESSIVE,

    /**
     * 宽松重试：任何失败都继续重试（含原本"不可重试"的错误），仅在重试耗尽后才判定该块失败。
     * 重试间隔 = 「当前词累计的失败次数 × 10 秒」，上限 2 分钟——失败越多等得越久，给瞬时故障（限流/过载）更多恢复时间。
     * 例：第一块失败 3 次、第二块失败 1 次时，下一次重试前等待 (3+1)×10 = 40 秒。
     */
    LENIENT
}
